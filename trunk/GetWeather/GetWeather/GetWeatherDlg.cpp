bool getgooglewhearth(CString strAreaNum)
{
	CInternetSession   *pInetSession=NULL;  
	CHttpConnection   *pHttpConnection=NULL;  
	CHttpFile	*pHttpFile = NULL;
	CString baseUrl;
	//baseUrl.Format("http://www.google.co.uk/ig/api?weather=%s",strAreaNum);   //英文
	baseUrl.Format("http://www.google.com.hk/ig/api?weather=%s",strAreaNum);    //汉语
	try
	{
		pInetSession=new CInternetSession(AfxGetAppName(),1,PRE_CONFIG_INTERNET_ACCESS);  
		pHttpFile = (CHttpFile*)pInetSession->OpenURL(baseUrl);
		int uCount = 0;
		char buf[2048]={0};	
		uCount = pHttpFile->Read(buf,sizeof(buf));
		OutputDebugString(buf);
	}
	catch(CInternetException   *pEx)  
	{  
		pEx->Delete();  
		delete pInetSession;  
		return false;
	} 
	delete pInetSession;  
	return true;
}

